package com.example.alumno.myapplication.models;

public class cliente {
    private String usermanem;
    private String firs_name;
    private String last_name;
    private int year_old;


    public String getUsermanem() {
        return usermanem;
    }

    public void setUsermanem(String usermanem) {
        this.usermanem = usermanem;
    }

    public String getFirs_name() {
        return firs_name;
    }

    public void setFirs_name(String firs_name) {
        this.firs_name = firs_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public int getYear_old() {
        return year_old;
    }

    public void setYear_old(int year_old) {
        this.year_old = year_old;
    }
}
